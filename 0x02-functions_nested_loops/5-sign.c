#include "main.h"
#include <stdio.h>
/**
 *print_sign - check if a char is alphabet or not
 * @n: is char to be checked
 * Return: 1 if positive, 0 if zero and -1 if negative
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	else
	{
		_putchar(n);
		return (0);
	}
}
